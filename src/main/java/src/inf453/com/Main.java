    private void jbtnAddActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jbtnAddActionPerformed
        String motFrancais = jtxtFRE.getText();
        String definition = jtxtDefinition.getText();
        String motSynonyme = jtxtSynonyms.getText();
        String motAnglais = jtxtENG.getText();
        String type = jComboType.getModel().getSelectedItem().toString();

        try {
            if (motAnglais.equals("") || type.equals("") || motSynonyme.equals("") || motFrancais.equals("")
                    || definition.equals("")) {
                JOptionPane.showMessageDialog(this, "Le(s) champ(s) sont vide(s)");
            } else {
                if (verifieTable.verifieDictionnaire(motFrancais, motAnglais, motSynonyme, type, definition) == true) {
                    JOptionPane.showMessageDialog(rootPane, "Dictionnaire deja existent");
                } else {

                    ajouteMot.dictionnaire(motFrancais.toLowerCase(), motAnglais.toLowerCase(),
                            motSynonyme.toLowerCase(), type, definition.toLowerCase());

                    if (verifieTable.verifieEquivalence(motAnglais, motFrancais) == false) {
                        ajouteMot.ajouteEquivalence(motFrancais.toLowerCase(), motAnglais.toLowerCase());
                    }
                    JOptionPane.showMessageDialog(rootPane, "mot add dans dictionnaire");
                }

            }
        } catch (HeadlessException | SQLException e) {
            System.err.println(e.getMessage());
        }
    }// GEN-LAST:event_jbtnAddActionPerformed

