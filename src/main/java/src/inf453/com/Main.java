    private void jButtonAddIrregulierActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButtonAddIrregulierActionPerformed
        String preterit = jtxtPreterit.getText();
        String motAnglais = jtxtEnglishVerb.getText();
        String motFrancais = jtxtMotFrancaisVI.getText();
        String pastParticiple = jtxtPastParticiple.getText();
        int ajouteVI;
        try {
            if (preterit.equals("") || motAnglais.equals("") || motFrancais.equals("") || pastParticiple.equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Le(s) champ(s) sont vide(s)");
            } else {

                ajouteVI = ajouteMot.verbeIrregulier(preterit.toLowerCase(), pastParticiple.toLowerCase(),
                        motAnglais.toLowerCase(), motFrancais.toLowerCase());

                if (verifieTable.verifieEquivalence(motAnglais, motFrancais) == false) {
                    ajouteMot.ajouteEquivalence(motFrancais.toLowerCase(), motAnglais.toLowerCase());
                }

                if (ajouteVI == 1) {
                    JOptionPane.showMessageDialog(this, "This verb has been added successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "Error! This verb hasn't been added!");
                }
            }
        } catch (SQLException e) {
            System.err.println(e.getMessage());

        }

    }// GEN-LAST:event_jButtonAddIrregulierActionPerformed

